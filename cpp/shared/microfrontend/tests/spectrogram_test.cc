TEST(Spectrogram, CheckConsecutiveWindow) {
  FrontendTestConfig config;
  struct FrontendState state;
  EXPECT_TRUE(
      FrontendPopulateState(&config.config_, &state, kSampleRate));
  size_t num_samples_read;
  size_t kStepSamples = kSampleRate * config.config_.window.step_size_ms / 1000;
  size_t kWindowSamples = kSampleRate * config.config_.window.size_ms / 1000;

  struct FrontendOutput output = FrontendProcessSamples(&state, kFakeAudioData,
                         sizeof(kFakeAudioData) / sizeof(kFakeAudioData[0]),
                         &num_samples_read);
  EXPECT_EQ(num_samples_read, kWindowSamples);

  size_t length = sizeof(kFakeAudioData) / sizeof(kFakeAudioData[0]) - kWindowSamples;
  int16_t *pointer = kFakeAudioData + kWindowSamples;
  size_t iteration = 1;
  
  FILE* fp;
  //EXPECT_TRUE( fp = fopen("spec.txt", "w") );
  
  if( fp != NULL ) {
    for (int i = 0; i < output.size; ++i) {
      //MICRO_EXPECT_EQ(output.values[i], expected[i]);
     // fprintf(fp, "%d ", output.values[i] );
    }
    //fprintf(fp,"\n");

    while( length >= kStepSamples ) {
      output = FrontendProcessSamples(
          &state, pointer,
          length,
          &num_samples_read);
      //printf("iteration=%d, length=%d, num_samples_read=%d\n",iteration,length,num_samples_read);
      EXPECT_EQ(num_samples_read, kStepSamples);
      EXPECT_EQ(output.size, 40);
      
      for (int i = 0; i < output.size; ++i) {
       // EXPECT_EQ(output.values[i], expected[i]);
       // fprintf(fp, "%d ", output.values[i] );
      }
     // fprintf(fp,"\n");
      
      length -= kStepSamples;
      pointer += kStepSamples;
      iteration ++;
    }
  }
  FrontendFreeStateContents(&state);
}


